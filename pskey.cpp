void Pskey::cnv(uint16_t buffer[])
{
    int i, off = 0;
    char *pstab[pskey_max];
    memset(pstab, 0, sizeof(pstab));
    while (off < buffer_size-4)
      {
        uint16_t keyid = buffer[off];
        uint16_t keyid2 = keyid & ~0x2000;
        uint16_t actchksum = buffer[off+1];
        uint16_t len = buffer[off+2];
        uint16_t *value = buffer+off+3;
        uint16_t i, expchksum = keyid + len;
        char ascbuf[len*5+10];
        *ascbuf = 0;
        if ((keyid != 0xFFFF) && (len != 0xFFFF))
          {
            for (i = 0; i < len; i++)
              {
                sprintf(ascbuf+strlen(ascbuf), "%.04x ", value[i]);
                expchksum += value[i];
              }
            if (keyid && (expchksum == actchksum))
              {
                char *strp;
                asprintf(&strp, "// %s\r\n&%.04x = %s\r\n", Pskey::nam(keyid2), keyid2, ascbuf);
                pstab[keyid2] = strp;
              }
            off += len+3;
          }
        else
          off += 4;
      }
    ofstream psf("psdump.ps");
    for (i = 0; i < pskey_max; i++)
      if (pstab[i])
        {
          psf << pstab[i];
        }
}

void Pskey::modify(uint16_t buffer[])
{
    int i, off = 0;
    int vm_disabled = 0;
    int offchip_hci = 0;
    while (off < buffer_size-4)
      {
        uint16_t keyid = buffer[off];
        uint16_t keyid2 = keyid & ~0x2000;
        uint16_t actchksum = buffer[off+1];
        uint16_t len = buffer[off+2];
        uint16_t *value = buffer+off+3;
        uint16_t i, expchksum = keyid + len;
        if ((keyid != 0xFFFF) && (len != 0xFFFF))
          {
            for (i = 0; i < len; i++)
              {
                expchksum += value[i];
              }
            if (keyid && (expchksum == actchksum)) switch(keyid2)
              {
              case PSKEY_VM_DISABLE:
                vm_disabled = 1;
                *value = 1;
                break;
              case PSKEY_ONCHIP_HCI_CLIENT:
                offchip_hci = 1;
                *value = 0;
                break;
              }
            off += len+3;
          }
        else if (keyid == 0xFFFF)
          {
            if (!vm_disabled)
              {
                buffer[off] = PSKEY_VM_DISABLE | 0x2000;
                buffer[off+2] = 1;
                buffer[off+3] = 1;
                buffer[off+1] = buffer[off] + buffer[off+2] + buffer[off+3];
                off += 4;
              }
            if (!offchip_hci)
              {
                buffer[off] = PSKEY_ONCHIP_HCI_CLIENT | 0x2000;
                buffer[off+2] = 1;
                buffer[off+3] = 0;
                buffer[off+1] = buffer[off] + buffer[off+2] + buffer[off+3];
                off += 4;
              }
            off = buffer_size;
          }
      }
    ofstream xdv("psmod.xdv");
    xdv << hex << setfill('0');    
    for (i = 0; i < buffer_size; i++)
        {
          xdv << "@" << setw(6) << 3*0x1000 + i
                 << " " << setw(4) << buffer[i] << '\r' << endl;
        }
}
